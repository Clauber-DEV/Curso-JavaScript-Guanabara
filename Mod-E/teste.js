// estrutura de repetição com teste lógico no final. do while == faça enquanto
var num = 1
do {
    console.log(`${num} repetição.`)
    num++
} while (num <= 6)


// estrutura de repetição com teste lógico no início. while == enquanto 
var num = 1
while (num <= 6) {
    console.log(`${num} Oi. tudo bem?`)
    num++
}




